import { ApiProperty } from '@nestjs/swagger';
import { BaseEntityDto } from '../common/base-entity.dto';
import { IsString, IsOptional, IsBoolean, IsObject, IsEnum } from 'class-validator';

export enum DocumentFormat {
  PDF = 'PDF',
  DOCX = 'DOCX',
  HTML = 'HTML'
}

export enum DocumentEventType {
  APPROVAL = 'APPROVAL',
  REJECTION = 'REJECTION',
  REMINDER = 'REMINDER',
  CONFIRMATION = 'CONFIRMATION',
  CANCELLATION = 'CANCELLATION'
}

export class CreateDocumentTemplateDto {
  @ApiProperty({ description: 'Template name', example: 'Approval Letter Template' })
  @IsString()
  name: string;

  @ApiProperty({ description: 'Template description', required: false })
  @IsOptional()
  @IsString()
  description?: string;

  @ApiProperty({ description: 'Resource type scope', required: false, example: 'ROOM' })
  @IsOptional()
  @IsString()
  resourceType?: string;

  @ApiProperty({ description: 'Category ID scope', required: false })
  @IsOptional()
  @IsString()
  categoryId?: string;

  @ApiProperty({ description: 'Event type', enum: DocumentEventType })
  @IsEnum(DocumentEventType)
  eventType: DocumentEventType;

  @ApiProperty({ description: 'Document format', enum: DocumentFormat })
  @IsEnum(DocumentFormat)
  format: DocumentFormat;

  @ApiProperty({ description: 'Template file path', required: false })
  @IsOptional()
  @IsString()
  templatePath?: string;

  @ApiProperty({ description: 'Template content with variables', required: false })
  @IsOptional()
  @IsString()
  content?: string;

  @ApiProperty({ description: 'Available variables', type: Object })
  @IsObject()
  variables: any;

  @ApiProperty({ description: 'Is default template', default: false })
  @IsOptional()
  @IsBoolean()
  isDefault?: boolean;

  @ApiProperty({ description: 'Can send as attachment', default: true })
  @IsOptional()
  @IsBoolean()
  canSendAsAttachment?: boolean;

  @ApiProperty({ description: 'Can send as link', default: true })
  @IsOptional()
  @IsBoolean()
  canSendAsLink?: boolean;

  @ApiProperty({ description: 'Created by user ID' })
  @IsString()
  createdBy: string;
}

